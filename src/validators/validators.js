export const correctDateFormat = () => {
   return (value) => {
      let regExp = /^(\d{1,2})[\s\.\/-](\d{1,2})[\s\.\/-](\d{4})$/
      if (regExp.test(value) === true) {
         return undefined
      }
      return 'Invalid date'
   }
}
