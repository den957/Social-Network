export const required = (value) => {
   if (value) {
      return undefined
   }
   else {
      return 'Require filed'
   }
}
export const minLengthCreator = (minLength) => {
   return (value) => {
      if (value && value.length > minLength) {
         return 'Field is too long'
      }
      return undefined
   }
}
